#include <stdio.h>
#include <stdlib.h>

/* Definindo os nos utilizados na fila. */
typedef struct No {
	int info;            /* Informacao a ser guardada na fila (PIDs). */
	struct No* prox;     /* Ponteiro para o proximo no da fila.       */
} nof;

/* Definindo a estrutura da fila. */
typedef struct Fi {
	nof *cabeca;  /* Inicio da fila.  */
	nof *fim;     /* Fim da fila.     */
	int n;        /* Tamanho da fila. */
} fila;

fila *novaFila();
int entra( fila *f, int info );
int proximo( fila *f );
int fechou( fila *f );
int tamanho( fila *f );
